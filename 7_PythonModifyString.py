# Upper case 
x = "my name is Samia and my father name is Sajid"
print(x.upper())

#Lowercase
x = "MY NAME IS Samia AND MY FATHER NAME IS Sajid"
print(x.lower())

# Removing Whitespace

x = "   Samia, Sajid "
print(x.strip())  

# Replace string 

x = "This is data  science tutorials"
print(x.replace("This" , "Bahi"))

# Split string 

x = "This, is, data , science, tutorials"
print(x.split(","))